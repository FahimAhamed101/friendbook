package com.app.myfriend.backend;

public class BackendHomeStory {
    public final String id;
    public final String name;
    public final String imageUrl;
    public final String badgeLabel;

    public BackendHomeStory(String id, String name, String imageUrl, String badgeLabel) {
        this.id = id;
        this.name = name;
        this.imageUrl = imageUrl;
        this.badgeLabel = badgeLabel;
    }
}
